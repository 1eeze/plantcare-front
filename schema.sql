CREATE OR REPLACE FUNCTION "public"."get_chat_list"() RETURNS TABLE("other_user_id" "uuid", "last_message_content" "text", "last_message_time" timestamp with time zone, "unread_count" bigint)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT
    sub.other_user_id,
    m.content AS last_message_content,
    m.created_at AS last_message_time,
    (
      SELECT COUNT(*)
      FROM messages unread_m
      WHERE unread_m.sender_id = sub.other_user_id
        AND unread_m.receiver_id = auth.uid()
        AND unread_m.is_read = false
    ) as unread_count
  FROM (
    SELECT
      DISTINCT ON (
        CASE
          WHEN sender_id = auth.uid() THEN receiver_id
          ELSE sender_id
        END
      )
      CASE
        WHEN sender_id = auth.uid() THEN receiver_id
        ELSE sender_id
      END as other_user_id,
      id as last_message_id
    FROM messages
    WHERE sender_id = auth.uid() OR receiver_id = auth.uid()
    ORDER BY other_user_id, created_at DESC
  ) AS sub
  JOIN messages m ON sub.last_message_id = m.id
  ORDER BY m.created_at DESC;
END;
$$;


