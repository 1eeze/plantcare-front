CREATE OR REPLACE FUNCTION "public"."insert_sensor_data"("p_plant_id" "uuid", "p_temp" "jsonb", "p_humidity" "jsonb", "p_light" "jsonb") RETURNS "uuid"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
begin
  insert into public.sensor_data (plant_id, temp, humidity, light, created_at)
  values (
    p_plant_id,
    jsonb_build_array(p_temp),
    jsonb_build_array(p_humidity),
    jsonb_build_array(p_light),
    now()
  )
  on conflict (plant_id) do update
  set temp       = jsonb_build_array(p_temp)     || coalesce(public.sensor_data.temp, '[]'::jsonb),
      humidity   = jsonb_build_array(p_humidity) || coalesce(public.sensor_data.humidity, '[]'::jsonb),
      light      = jsonb_build_array(p_light)    || coalesce(public.sensor_data.light, '[]'::jsonb),
      created_at = now();

  return p_plant_id;
end;
$$;


